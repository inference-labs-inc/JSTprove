use std::collections::HashMap;
use std::io::{self, BufRead};

const ELEMENT_BUCKETS: &[(u64, &str)] = &[
    (10_000, "10_000"),
    (50_000, "50_000"),
    (100_000, "100_000"),
    (500_000, "500_000"),
    (u32::MAX as u64, "u32::MAX"),
];

#[allow(
    clippy::cast_possible_truncation,
    clippy::cast_sign_loss,
    clippy::single_match_else
)]
fn main() {
    let stdin = io::stdin();
    let mut by_model: HashMap<(u32, String), Vec<(usize, usize)>> = HashMap::new();

    for line in stdin.lock().lines() {
        let Ok(line) = line else { continue };
        let line = line.trim().to_string();
        if !line.starts_with('{') {
            continue;
        }

        let Ok(v) = serde_json::from_str::<serde_json::Value>(&line) else {
            continue;
        };

        let kappa = v["kappa"].as_u64().unwrap() as u32;
        let model = v["model"].as_str().unwrap().to_string();
        let chunk_bits = v["chunk_bits"].as_u64().unwrap() as usize;
        let circuit_bytes = v["circuit_bytes"].as_u64().unwrap() as usize;

        by_model
            .entry((kappa, model))
            .or_default()
            .push((chunk_bits, circuit_bytes));
    }

    if by_model.is_empty() {
        eprintln!("no records on stdin");
        std::process::exit(1);
    }

    let mut best_by_kappa: HashMap<u32, Vec<usize>> = HashMap::new();
    for ((kappa, _model), results) in &by_model {
        let best_chunk = results
            .iter()
            .min_by_key(|(_chunk, bytes)| *bytes)
            .unwrap()
            .0;
        best_by_kappa.entry(*kappa).or_default().push(best_chunk);
    }

    let mut optimal: Vec<(u32, usize)> = Vec::new();
    for (kappa, winners) in &best_by_kappa {
        let mut counts: HashMap<usize, usize> = HashMap::new();
        for &w in winners {
            *counts.entry(w).or_default() += 1;
        }
        let best = counts
            .into_iter()
            .max_by_key(|(_chunk, count)| *count)
            .unwrap()
            .0;
        optimal.push((*kappa, best));
    }
    optimal.sort_unstable();

    println!("struct Entry {{");
    println!("    kappa: u8,");
    println!("    n_bits: u8,");
    println!("    max_elements: u32,");
    println!("    chunk_bits: u8,");
    println!("}}");
    println!();
    println!("// Generated by logup_codegen from logup_sweep output.");
    println!(
        "// To regenerate: cargo run --release --bin logup_sweep | cargo run --release --bin logup_codegen"
    );
    println!("static TABLE: &[Entry] = &[");

    for &(kappa, chunk) in &optimal {
        for n_bits in [64, 31] {
            for &(_, label) in ELEMENT_BUCKETS {
                println!(
                    "    Entry {{ kappa: {kappa}, n_bits: {n_bits}, max_elements: {label}, chunk_bits: {chunk} }},",
                );
            }
        }
    }

    println!("];");
    println!();
    println!("#[must_use]");
    println!("pub fn lookup(kappa: usize, n_bits: usize, estimated_elements: usize) -> usize {{");
    println!("    for entry in TABLE {{");
    println!("        if entry.kappa as usize == kappa");
    println!("            && entry.n_bits as usize == n_bits");
    println!("            && estimated_elements <= entry.max_elements as usize");
    println!("        {{");
    println!("            return entry.chunk_bits as usize;");
    println!("        }}");
    println!("    }}");
    println!("    super::DEFAULT_LOGUP_CHUNK_BITS");
    println!("}}");
}
